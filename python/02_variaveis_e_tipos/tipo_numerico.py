""" Tipo numérico """

num1 = 10
num2 = 5

# Restorna a divisão 
divisao = num1 / num2

# Retorna apenas a parte inteira da divisão (casas decimas antes da vírgula)
divisao_inteira = num1 // num2

# Retorna o resto de um divisão inteira (caso haja)
resto_divisao = num1 % num2

# Multiplicação
multiplicacao = num1 * num2

# Soma
soma = num1 + num2

# Subtração 
subtracao = num1 - num2

# Potenciação 
potenciacao = num1 ** num2
